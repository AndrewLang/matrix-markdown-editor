export const sum = function (a: number, b: number) {
    return a + b;
  }
  export const sub = function (a: number, b: number) {
    return a - b;
  }
  export const mutiply = function (a: number, b: number) {
    return a * b;
  }
  
  export class Calculator {
    add(a: number, b: number) {
      return a + b;
    }
  }